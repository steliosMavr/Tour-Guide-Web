
package gr.javabeans;


public class Cars {
    
    private String licenceNumber;
    private String kv;
    private String passengerSits;
    private String category;
    private String chargePerDay;
    private String increaseCharge;

    public String getLicenceNumber() {
        return licenceNumber;
    }

    public void setLicenceNumber(String licenceNumber) {
        this.licenceNumber = licenceNumber;
    }

    public String getKv() {
        return kv;
    }

    public void setKv(String kv) {
        this.kv = kv;
    }

    public String getPassengerSits() {
        return passengerSits;
    }

    public void setPassengerSits(String passengerSits) {
        this.passengerSits = passengerSits;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getChargePerDay() {
        return chargePerDay;
    }

    public void setChargePerDay(String chargePerDay) {
        this.chargePerDay = chargePerDay;
    }

    public String getIncreaseCharge() {
        return increaseCharge;
    }

    public void setIncreaseCharge(String increaseCharge) {
        this.increaseCharge = increaseCharge;
    }
    
    
    
    
    
}
