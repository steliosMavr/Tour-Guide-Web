package gr.javabeans;

public class MotorBikes {

    private String motoLicenceNumber;
    private String motoKv;
    private String category;
    private String chargePerDay;
    private String increaseCharge;

    public String getMotoLicenceNumber() {
        return motoLicenceNumber;
    }

    public void setMotoLicenceNumber(String motoLicenceNumber) {
        this.motoLicenceNumber = motoLicenceNumber;
    }

    public String getMotoKv() {
        return motoKv;
    }

    public void setMotoKv(String motoKv) {
        this.motoKv = motoKv;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getChargePerDay() {
        return chargePerDay;
    }

    public void setChargePerDay(String chargePerDay) {
        this.chargePerDay = chargePerDay;
    }

    public String getIncreaseCharge() {
        return increaseCharge;
    }

    public void setIncreaseCharge(String increaseCharge) {
        this.increaseCharge = increaseCharge;
    }

    
    
    
    
    
    
}
